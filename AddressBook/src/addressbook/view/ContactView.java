    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = this.txtName.getText();
        String surname = this.txtSurname.getText();
        String number = this.txtNumber.getText();
        String address = this.txtAddress.getText();
        String age = this.txtAge.getText();
        if(controller.checkFields(name, surname, number))
            JOptionPane.showMessageDialog(this,
                    "I campi nome, cognome e telefono sono obbligatori.", 
                    "Errore",JOptionPane.WARNING_MESSAGE);
        else{
            controller.updateContacts(name, surname, number, address, age, mode);
            this.contactsView.initJTable();
            this.dispose();
        }    
    }//GEN-LAST:event_btnSaveActionPerformed

